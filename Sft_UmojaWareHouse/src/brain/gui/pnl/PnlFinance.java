    private void getFinanceData(JLabel lbMt, JLabel lbDt)
    {
        try
        {
            PreparedStatement ps = DbConnect.connectDb().prepareStatement("SELECT * FROM tFinance_Caisse");
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                  lbMt.setText(rs.getFloat("montant")+"");
                  lbDt.setText(rs.getDate("date_UPDATE")+"");
            }
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, "Error :\n"+e.getMessage(), "fetch_Entry Loading Error", JOptionPane.WARNING_MESSAGE);
        }
    }
